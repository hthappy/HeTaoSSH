/**
 * IPC 通信相关常量
 * 
 * 这些常量用于控制前后端 IPC 通信的行为，包括防抖、超时等
 */

/**
 * IPC 防抖延迟（毫秒）
 * 
 * 用于终端输入防抖，在短时间内累积多个按键后一次性发送到后端
 * 5ms 的延迟对用户来说几乎无感知，但可以有效减少 IPC 调用次数
 */
export const IPC_DEBOUNCE_MS = 5;

/**
 * IPC 最大等待时间（毫秒）
 * 
 * 防抖的最大等待时间，确保即使持续有输入也会定期发送数据
 */
export const IPC_MAX_WAIT_MS = 150;

/**
 * 文件上传分块大小（字节）
 * 
 * SFTP 文件上传时的分块大小，32KB 是一个平衡性能和内存的合理值
 */
export const UPLOAD_CHUNK_SIZE = 32768; // 32KB
